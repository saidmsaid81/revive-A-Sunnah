"use strict";

var insomniaEnabled = false;

function insomniaDemo() {
  // toggle
  insomniaEnabled = !insomniaEnabled;

  // call plugin
  if (insomniaEnabled) {
    window.plugins.insomnia.keepAwake(function(a) {alert('The screen will be on as you read: ' + a)}, function(b) {alert('keepAwake error: ' + b)});
  } else {
    window.plugins.insomnia.allowSleepAgain(function(a) {alert('Phone allowed to sleep: ' + a)}, function(b) {alert('allowSleepAgain error: ' + b)});
  }
}

